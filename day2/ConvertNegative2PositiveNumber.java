package week1.day2;

public class ConvertNegative2PositiveNumber {
	public static void main(String[] args) {
		int i=-548;
		
		if(i<0) {
			System.out.println("Given Negative number is:"    +i);
		int j=i*-1;
		
		System.out.println("After converting from negative number to positive number:"     +j);
		}else
		{
			System.out.println("The given number is not Negative number");
		}
	}

}
